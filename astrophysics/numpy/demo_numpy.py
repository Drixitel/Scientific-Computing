import numpy as np 

x = 1.0
y = 2.0 

#trig 
print(np.sin(x)) #in rad
print(np.cos(x))
print(np.tan(x))
print(np.arcsin(x))
print(np.arccos(x))
print(np.arctan(x))
print(np.arctan2(x,y)) #arctan(x/y)
print(np.rad2deg(x)) #convert rad x to degrees 


#hyperbolic functions 
print(np.sinh(x))
print(np.cosh(x))
print(np.tanh(x))
print(np.arcsinh(x))
print(np.arccosh(x))
print(np.arctanh(x))
